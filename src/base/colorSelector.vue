<template>
  <div>
    <el-tag
      :type="type"
      :key="tag"
      v-for="tag in dynamicTags"
      closable
      @close="handleClose(tag)">
      {{tag}}
    </el-tag>
    <el-input
      class="input-new-tag"
      v-if="inputVisible"
      v-model="inputValue"
      ref="saveTagInput"
      size="small"
      @keyup.enter.native="handleInputConfirm"
      @blur="handleInputConfirm"
    >
    </el-input>
    <el-button v-else class="button-new-tag" size="small" @click="showInput">+添加{{tip}}</el-button>
  </div>
</template>

<script>
  export default {
    props: {
      tip:{
        type:String
      },
      dynamicTags: {
        type: Array
      },
      type:{
         type:String
      }
    },
    data() {
      return {

        inputVisible: false,
        inputValue: ''
      };
    },
    methods: {
      handleClose(tag) {
        this.dynamicTags.splice(this.dynamicTags.indexOf(tag), 1);
      },

      showInput() {
        this.inputVisible = true;
        this.$nextTick(_ => {
          this.$refs.saveTagInput.$refs.input.focus();
        });
      },

      handleInputConfirm() {
        let inputValue = this.inputValue;
        if (inputValue) {
          this.dynamicTags.push(inputValue);
          console.log(this.dynamicTags);
        }
        this.inputVisible = false;
        this.inputValue = '';
      },
    },
  }
</script>

